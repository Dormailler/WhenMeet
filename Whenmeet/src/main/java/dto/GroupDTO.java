package dto;

public class GroupDTO {
	String group_id, group_name, group_create_time, group_description, final_schedule, final_location, project_end_time;
	
	public String getGroup_id() {
		return group_id;
	}
	public void setGroup_id(String group_id) {
		this.group_id = group_id;
	}
	public String getGroup_name() {
		return group_name;
	}
	public void setGroup_name(String group_name) {
		this.group_name = group_name;
	}
	public String getGroup_create_time() {
		return group_create_time;
	}
	public void setGroup_create_time(String group_create_time) {
		this.group_create_time = group_create_time;
	}
	public String getGroup_description() {
		return group_description;
	}
	public void setGroup_description(String group_description) {
		this.group_description = group_description;
	}
	public String getFinal_schedule() {
		return final_schedule;
	}
	public void setFinal_schedule(String final_schedule) {
		this.final_schedule = final_schedule;
	}
	public String getFinal_location() {
		return final_location;
	}
	public void setFinal_location(String final_location) {
		this.final_location = final_location;
	}
	public String getProject_end_time() {
		return project_end_time;
	}
	public void setProject_end_time(String project_end_time) {
		this.project_end_time = project_end_time;
	}
	
	
}
